const testInitState = {
    //error: new Error("Error"),
    isLoading: false,
    content: testResultDetails
}

const initState = {
    isLoading: false,
    content: {}
}

export const resultDetailsReducer = (state = (dev ? testInitState : initState), action) => {
    switch (action.type) {
        case "LOADING_RESULT_DETAILS": {
            return {...state, isLoading: action.isLoading};
        }
        case "LOADING_RESULT_DETAILS_FAILURE": {
            return {...state, error: action.error};
        }
        case "CLEAR_LOADING_RESULT_DETAILS_FAILURE": {
            return {...state, error: null};
        }
        case "CLEAR_ALL_RESULT_DETAILS_FAILURES": {
            return {...state, error: null};
        }
        case "SET_RESULT_DETAILS": {
            let resultId = action.resultId;
            let resultDetails = action.payload;
            return {...state, content: {...state.content, [resultId]: resultDetails.questionResults}};
        }
        default:
            return state;
    }
}